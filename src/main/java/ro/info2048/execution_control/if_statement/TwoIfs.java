package ro.info2048.execution_control.if_statement;

public class TwoIfs {
    public static void main(String[] args) {
        int age = 25;
        if (age > 60) {
            System.out.println("This is an old man!");
        }
        if (age <= 60) {
            System.out.println("This is a young man!");
        }
    }
}
