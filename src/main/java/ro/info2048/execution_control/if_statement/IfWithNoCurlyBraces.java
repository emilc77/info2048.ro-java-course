package ro.info2048.execution_control.if_statement;

public class IfWithNoCurlyBraces {
    public static void main(String[] args) {
        int age = 78;
        if (age > 60)
            System.out.println("This is an old man!");
            System.out.println("Yet very healthy");
        if (age < 30)
            System.out.println("This is a young person!");
            System.out.println("We may envy him");
    }
}
