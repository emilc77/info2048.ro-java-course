
package ro.info2048.primitive.no_decimals;

public class OperatorMultiplyWithEquals {
    public static void main(String[] args) {
        int i = 1;
        System.out.println(i); // 1
        i = i * 5;
        System.out.println(i); // 5
        i *= 5;
        System.out.println(i); // 25
    }
}
