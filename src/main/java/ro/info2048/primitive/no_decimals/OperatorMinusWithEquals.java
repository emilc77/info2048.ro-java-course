package ro.info2048.primitive.no_decimals;

public class OperatorMinusWithEquals {
    public static void main(String[] args) {
        int i = 11;
        System.out.println(i); // 11
        i = i - 5;
        System.out.println(i); // 6
        i -= 5;
        System.out.println(i); // 1
    }
}
