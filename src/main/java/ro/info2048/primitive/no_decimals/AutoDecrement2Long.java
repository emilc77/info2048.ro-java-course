package ro.info2048.primitive.no_decimals;

public class AutoDecrement2Long {
    public static void main(String[] args) {
        int a = 10;

        System.out.println(a--); // 10
        System.out.println(a); // 9
        System.out.println(--a); // 8
        System.out.println(a); // 8
    }
}
