package ro.info2048.primitive.bool;

public class LogicalOperatorOr {
    public static void main(String[] args) {
        boolean pisicaRacita = true;
        boolean catelCuPurici = false;

        boolean mergemVeterinar = pisicaRacita || catelCuPurici;
        System.out.println(mergemVeterinar); // true
    }
}
